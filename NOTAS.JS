let nota = Number(prompt("Digite uma nota de 1 a 5:"));

switch (nota) {
  case 1:
    console.log("Muito ruim");
    break;

  case 2:
    console.log("Ruim");
    break;

  case 3:
    console.log("Regular");
    break;

  case 4:
    console.log("Bom");
    break;

  case 5:
    console.log("Excelente");
    break;

  default:
    console.log("Nota inválida");
}